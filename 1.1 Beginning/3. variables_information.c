// A variable is name given to a space in memory where you can store a value
// Basic format of defining a variable is
// datatype variable_name;
// For example to define an integer datatype with variable name a
// int a;
// a = 6;
// OR
// int a = 6;
// Rules for Naming Variables
// Start with a letter or underscore (_)
// Don’t use spaces or special characters
// Can’t be a C keyword like int, return, etc.
// Use meaningful names (e.g., marks, not x)

/* Datatypes in C
 * int - to store integer value
 * float - to save decimal value (4 byte)
 * char - to store character (only one character)
 * double - more precise decimal number
 */

/* Format Specifiers for different datatypes, to print for example int a, we need to type printf("%d", a) inside ""
 * int - %d
 * float - %f
 * double - %lf
 * char - %c
 * float (upto 2 decimal places only) - %.2f
 */

/* The variables consume different amount of memory in RAM
 * int - 4 byte
 * char - 1 byte
 * float - 4 byte
 * double - 8 byte 
 */